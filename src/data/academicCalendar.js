export const getEventsForDate = (year, month, day) => {
  return academicEvents[year]?.[month]?.[day] || [];
};

export const hasEvents = (year, month, day) => {
  return (academicEvents[year]?.[month]?.[day]?.length || 0) > 0;
};

export const getMonthEvents = (year, month) => {
  const monthEvents = academicEvents[year]?.[month] || {};
  const events = [];
  
  Object.entries(monthEvents).forEach(([day, dayEvents]) => {
    dayEvents.forEach(event => {
      events.push({
        ...event,
        day: parseInt(day),
        date: new Date(year, month - 1, day)
      });
    });
  });
  
  return events.sort((a, b) => a.day - b.day);
};